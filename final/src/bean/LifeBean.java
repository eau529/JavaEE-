package bean;

import java.util.Date;

public class LifeBean {
    private String id;
    private String body;
    private Date date;
    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }



    public String getBody() {
        return body;
    }

    public void setBody(String body) {
        this.body = body;
    }

    public Date getDate() {
        return date;
    }

    public void setDate(Date date) {
        this.date = date;
    }


}
